function mostrarEOD()
{
    if(document.getElementById('extordia').checked)
    {
        document.getElementById('horasextorddia').style.visibility = 'visible';
    }
    else
    {
        document.getElementById('horasextorddia').style.visibility = 'hidden';
    }
}

